package practice33;

class Coordinate {
	private double x;
	private double y;

	Coordinate(double x, double y){
		this.x=x;
		this.y=y;
	}

	double getX() {
		return x;
	}
	double getY() {
		return y;
	}
	void setX() {
		this.x=x;
	}
	void setY() {
		this.y=y;
	}
	void set(double x , double y) {
		this.x=x;
		this.y=y;
	}
}
